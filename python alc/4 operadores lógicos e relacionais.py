#operadores relacionais

"""
== igual
!= diferente
>= maior ou igual
<= menor ou igual
"""
x = 2
y = 3
a = 3
b = 5
soma = x + y

print(x == y)
print(a == y)
print(x > y)
print(soma != b)

#operadores lógicos

"""
AND  comparar se duas condições são verdadeiras
OR   comparar se pelo menos uma condição é verdadeira
NOT  vai inverter o resulado dessas condições
"""

print(a == y and b > y)
print(x == y or a == y)

